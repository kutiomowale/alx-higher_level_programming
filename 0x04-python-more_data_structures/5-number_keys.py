#!/usr/bin/python3

"""Number of keys

A module that defines a function, that returns
the number of keys in a dictionary.
"""


def number_keys(a_dictionary):
    """Number of keys

    A function that returns the number of keys in a dictionary

    Args:
        a_dictionary (dictionary): The dictionary

    Returns:
        The number of keys
    """
    # The number of keys or values in a dictionary is its length
    return len(a_dictionary)
